// -----------------------------------------------------------------------------
// NODE, EXPRESS, MIDDLEWARE MODULES
// -----------------------------------------------------------------------------

// Dependencies
const express       = require('express')
const bodyParser    = require('body-parser')
const cors          = require('cors')
const logger        = require('morgan')
const app           = express()

// -----------------------------------------------------------------------------
// APP MODULES
// -----------------------------------------------------------------------------

// Routes
const apiProducts = require('./routes/api_products.js')

// -----------------------------------------------------------------------------
// APP CONFIGURATION
// -----------------------------------------------------------------------------

//Express Middlewares
app.use(bodyParser.json())
app.use(logger('dev'))
app.use(cors())

// -----------------------------------------------------------------------------
// REGISTER ROUTES
// -----------------------------------------------------------------------------

app.use('/', apiProducts)

// -----------------------------------------------------------------------------
// RUN THE APP
// -----------------------------------------------------------------------------

app.listen(8888, () => console.log('Server listening on port 8888'))